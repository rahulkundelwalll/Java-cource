public class Rational {
    public static void main(String[] args) {
        int a = 10;
        int b = 100;
        System.out.println(a==b); // check the eqaulity
        System.out.println(a!=b); //  not equal
        System.out.println(a>b); // a greater b
        System.out.println(a<b); // a less than b
        System.out.println(a>=b); // a greater than equal to b
        System.out.println(a<=b); // a less than equal to b
      
        
    }
}
